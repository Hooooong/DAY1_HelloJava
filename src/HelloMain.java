public class HelloMain {
	
	/**
	 * 함수의 작성 형태
	 * 
	 * 접근제한자	(static)	리턴타입	함수명() {
	 * public		
	 * private
	 * protected
	 * (default)  
	 * }
	 */
	public static void main(String[] args) {
		System.out.println(2 + 3);
		System.out.println(2 - 3);
		System.out.println(2 * 3);
		System.out.println(2 / 3);
		System.out.println(5 % 3);
		System.out.println(10 % 3);
		System.out.println(5.0 % 4.2);
	}
}

